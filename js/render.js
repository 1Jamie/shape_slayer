// Rendering functions

// Particle class for visual effects
class Particle {
    constructor(x, y, vx, vy, color, size, life) {
        this.x = x;
        this.y = y;
        this.vx = vx;
        this.vy = vy;
        this.color = color;
        this.size = size;
        this.life = life;
        this.maxLife = life;
        this.alpha = 1.0;
    }
    
    update(deltaTime) {
        this.x += this.vx * deltaTime;
        this.y += this.vy * deltaTime;
        this.life -= deltaTime;
        
        // Fade out over time
        this.alpha = this.life / this.maxLife;
        
        // Apply gravity
        this.vy += 200 * deltaTime;
        
        return this.life > 0;
    }
    
    render(ctx) {
        ctx.save();
        ctx.globalAlpha = this.alpha;
        ctx.fillStyle = this.color;
        ctx.beginPath();
        ctx.arc(this.x, this.y, this.size, 0, Math.PI * 2);
        ctx.fill();
        ctx.restore();
    }
}

// Particle pooling
const particlePool = [];
const MAX_POOL_SIZE = 200;

function getParticle() {
    if (particlePool.length > 0) {
        return particlePool.pop();
    }
    return null;
}

function releaseParticle(p) {
    if (particlePool.length < MAX_POOL_SIZE) {
        particlePool.push(p);
    }
}

// Create burst of particles at position
function createParticleBurst(x, y, color, count = 10) {
    if (typeof Game === 'undefined') return;
    if (!Game.particles) Game.particles = [];
    
    for (let i = 0; i < count; i++) {
        const angle = (Math.PI * 2 / count) * i + Math.random() * 0.5;
        const speed = 100 + Math.random() * 100;
        const vx = Math.cos(angle) * speed;
        const vy = Math.sin(angle) * speed;
        
        let particle = getParticle();
        if (!particle) {
            particle = new Particle(x, y, vx, vy, color, 3 + Math.random() * 3, 0.5);
        } else {
            particle.x = x;
            particle.y = y;
            particle.vx = vx;
            particle.vy = vy;
            particle.color = color;
            particle.size = 3 + Math.random() * 3;
            particle.life = 0.5;
            particle.maxLife = 0.5;
            particle.alpha = 1.0;
        }
        
        Game.particles.push(particle);
    }
}

// Update and render particles
function updateParticles(deltaTime) {
    if (!Game || !Game.particles) return;
    
    Game.particles = Game.particles.filter(particle => {
        const alive = particle.update(deltaTime);
        if (!alive) {
            releaseParticle(particle);
        }
        return alive;
    });
}

function renderParticles(ctx) {
    if (!Game || !Game.particles) return;
    
    Game.particles.forEach(particle => {
        particle.render(ctx);
    });
}

